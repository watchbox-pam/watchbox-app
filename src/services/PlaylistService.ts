import Playlist from "@/src/models/Playlist";
import { ApiHelper } from "@/src/utils/axios";

/**
 * Creates a new playlist for a user.
 * Validates required fields before sending the request.
 * @param playlist Playlist object containing title, privacy status, and user ID
 * @returns Operation result with success status and message
 */
export async function createPlaylist(playlist: Playlist) {
	try {
		if (!playlist.title) {
			return {
				success: false,
				message: "Le titre de la playlist est requis",
				element: "title"
			};
		}

		if (playlist.is_private === undefined || playlist.is_private === null) {
			return {
				success: false,
				message: "Le statut de confidentialité est requis",
				element: "is_private"
			};
		}

		if (!playlist.user_id) {
			return {
				success: false,
				message: "L'identifiant de l'utilisateur est requis",
				element: "userId"
			};
		}

		// Send POST request to create a new playlist
		const result = await ApiHelper.post("/playlists/", {
			id: "", // Usually generated by backend
			user_id: playlist.user_id,
			title: playlist.title,
			created_at: new Date().toISOString(),
			is_private: playlist.is_private
		});

		// Return success or error message based on API response
		if (result.success) {
			return {
				success: true,
				message: "Playlist créée avec succès"
			};
		} else {
			return {
				success: false,
				message:
					result.data || "Erreur lors de la création de la playlist"
			};
		}
	} catch (error) {
		return {
			success: false,
			// @ts-ignore
			message: error.detail || "Une erreur est survenue"
		};
	}
}

/**
 * Updates an existing playlist.
 * Checks for restricted playlist names.
 * @param playlist Playlist object with updated data (must include id)
 * @returns Operation result with success status and message
 */
export async function updatePlaylist(playlist: Playlist) {
	if (!playlist.id) {
		return {
			success: false,
			message: "L'identifiant de la playlist est requis",
			element: "id"
		};
	}

	try {
		// Prevent certain reserved playlist names
		const restrictedNames = ["watchlist", "historique", "favoris"];
		const toLowerCaseTitle = playlist.title.toLowerCase();
		if (restrictedNames.includes(toLowerCaseTitle)) {
			return {
				success: false,
				message: "Veuillez choisir un autre nom de playlist",
				element: "title"
			};
		}

		// Send PUT request to update playlist
		const result = await ApiHelper.put(`/playlists/${playlist.id}`, {
			id: playlist.id,
			user_id: playlist.user_id,
			title: playlist.title,
			is_private: playlist.is_private
		});

		// Return success or error message based on API response
		if (result.success) {
			return {
				success: true,
				message: "Playlist mise à jour avec succès"
			};
		} else {
			return {
				success: false,
				message:
					result.data ||
					"Erreur lors de la mise à jour de la playlist"
			};
		}
	} catch (error) {
		const err = error as any;
		console.error("Error during updatePlaylist:", err);
		return {
			success: false,
			message:
				err.message || "Erreur lors de la mise à jour de la playlist"
		};
	}
}

