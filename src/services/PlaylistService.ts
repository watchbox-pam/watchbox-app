/**
 * Adds a media item (movie, show, etc.) to a playlist.
 * @param playlistId Playlist identifier
 * @param mediaId Media item identifier
 * @returns Operation result with success status and message
 */
export async function addMediaToPlaylist(playlistId: string, mediaId: number) {
	if (!playlistId || typeof playlistId !== "string") {
		return {
			success: false,
			message: "ID de la playlist invalide"
		};
	}

	if (!mediaId || typeof mediaId !== "number") {
		return {
			success: false,
			message: "ID du média invalide"
		};
	}

	try {
		const result = await ApiHelper.post(
			`/playlists/${playlistId}/media/${mediaId}`,
			{
				playlist_id: playlistId,
				media_id: mediaId
			}
		);

		if (result && result.success) {
			return {
				success: true,
				message: "Média ajouté à la playlist avec succès"
			};
		} else {
			return {
				success: false,
				message:
					result?.data?.detail ||
					"Film déjà existant dans la playlist"
			};
		}
	} catch (error: any) {
		console.error("Error in addMediaToPlaylist:", error);
		return {
			success: false,
			message:
				error?.response?.data?.detail ||
				error.message ||
				"Erreur inconnue lors de l'ajout du média"
		};
	}
}

